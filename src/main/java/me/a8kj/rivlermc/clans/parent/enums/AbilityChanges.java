package me.a8kj.rivlermc.clans.parent.enums;

/**
 * Enumeration representing the different types of changes that can be made to
 * abilities.
 */
public enum AbilityChanges {
    /**
     * Represents the addition of a new ability.
     */
    ADD,

    /**
     * Represents the removal of an existing ability.
     */
    REMOVE,

    /**
     * Represents clearing all abilities.
     */
    CLEAR_ALL,

    /**
     * Represents setting or updating abilities.
     */
    SET;
}
